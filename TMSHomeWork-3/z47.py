import turtle
import time
t = turtle.Turtle()
t.speed(30)
t.goto(0, 0)
t.circle(50)
t.up()
t.goto(0, 100)
t.down()
t.left(90)
t.forward(30)
t.up()
t.goto(0, 0)
t.down()
t.right(180)
t.forward(30)
t.up()
t.goto(50, 50)
t.down()
t.left(90)
t.forward(30)
t.up()
t.goto(-50, 50)
t.down()
t.left(180)
t.forward(30)
for i in range(61):
	t.speed(10)
	t.up()
	t.goto(0, 50)
	t.right(90)
	t.down()
	t.forward(25)
	time.sleep(3)
	t.undo()
turtle.mainloop()
